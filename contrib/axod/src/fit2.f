      VZ1AH=STVZ1(L)*ANN1(I,K)/ANN1A(I,K)
      STRU1A(L)=VU1AH-U1A(I,K)/R3
      STBET1(L)=57.29578*ATAN(STRU1A(L)/VZ1AH)
      T=TALF-(TALF/R3 - SIN(RADRD(I,K))/COS(RADRD(I,K)))/R3
