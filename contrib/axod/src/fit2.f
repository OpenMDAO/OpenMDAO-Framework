      cat1=cos(atan(tangm1(ll,k)))
      cat2=cos(atan(tangm2(ll,k)))
      FAXS(L )=((STPS0(L )-STPS1(L ))*(ANN1(I  ,K) +ANN0(I  ,K))*72.
     1+WG1(I  ,K)/G*(STVZ0(L )*cat0-STVZ1(L )*cat1))/HBRS
