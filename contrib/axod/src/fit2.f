     1/DBARR(L )/G/HBRR*TFR(I  ,K)
      FAXR(L)=((PS1AH-PS2H)*(ANN1A(I,K)+ANN2(I,K))*72.+
     1WG2(I,K)/G*(VZ1AH*cat1-VZ2H*cat2))/HBRR
